package dziedziczenie;

public class While {
    private String name;
    private int age;
    private double weight;

    public While(String name, int age, int weight) {
        this.name = name;
        this.age = age;
        this.weight = weight;
    }

    public While() {
        name = "Kraken";
        age = 60;
        weight = 4.5;
    }

}
