package dziedziczenie;

public class Monkey {
    private String name;
    private int age;
    private int tailLength;

    public Monkey(String name, int age, int tailLength) {
        this.name = name;
        this.age = age;
        this.tailLength = tailLength;
    }

    public Monkey(){
        name = "Jacek";
        age = 8;
        tailLength = 20;


    }
}
